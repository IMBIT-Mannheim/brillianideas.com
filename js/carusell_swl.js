positionNum_swl = 0;
currentSlideAtPosMiddleLeftCounter_swl = 0;
currentSlideAtPosMiddleRightCounter_swl = 0;

function executeMiddleRightClick_swl() {
  declareSlideToBeMovedFromMiddleRight_swl(
    currentSlideAtPosMiddleRightCounter_swl
  );
}

function executeMiddleleftClick_swl() {
  declareSlideToBeMovedFromMiddleLeft_swl(
    currentSlideAtPosMiddleLeftCounter_swl
  );
}

async function sliderShow_swl() {
  while (positionNum_swl < 10) {
    //while loop bis Bedingung erfÃ¼llt ist
    await transitionBreakTimeLaunch();
    endlessLoop_swl();
  }
}

function endlessLoop_swl() {
  switch (positionNum_swl) {
    case 0:
      sliderTransitionMove21();
      positionNum_swl++;
      currentSlideAtPosMiddleLeftCounter_swl++;
      currentSlideAtPosMiddleRightCounter_swl++;
      break;
    case 1:
      sliderTransitionMove22();
      positionNum_swl++;
      currentSlideAtPosMiddleLeftCounter_swl++;
      currentSlideAtPosMiddleRightCounter_swl++;
      break;
    case 2:
      sliderTransitionMove23();
      positionNum_swl++;
      currentSlideAtPosMiddleLeftCounter_swl++;
      currentSlideAtPosMiddleRightCounter_swl++;
      break;
    case 3:
      sliderTransitionMove24();
      positionNum_swl = 0;
      currentSlideAtPosMiddleLeftCounter_swl = 0;
      currentSlideAtPosMiddleRightCounter_swl = 0;
      break;
    case 4:
      sliderTransitionMove21_right();
      positionNum_swl++;
      break;
    case 5:
      sliderTransitionMove22_right();
      positionNum_swl++;
      break;
    case 6:
      sliderTransitionMove23_right();
      positionNum_swl++;
      break;
    case 7:
      sliderTransitionMove24_right();
      positionNum_swl = 0;
      break;
  }
}

function declareSlideToBeMovedFromMiddleLeft_swl(
  currentSlideAtPosMiddleLeftCounter_swl
) {
  switch (currentSlideAtPosMiddleLeftCounter_swl) {
    case 0:
      onClickMovement_slide22(1);
      break;
    case 1:
      onClickMovement_slide21(1);
      break;
    case 2:
      onClickMovement_slide24(1);
      break;
    case 3:
      onClickMovement_slide23(1);
      break;
  }
}

function declareSlideToBeMovedFromMiddleRight_swl(
  currentSlideAtPosMiddleRightCounter_swl
) {
  switch (currentSlideAtPosMiddleRightCounter_swl) {
    case 0:
      onClickMovement_slide24(2);
      break;
    case 1:
      onClickMovement_slide23(2);
      break;
    case 2:
      onClickMovement_slide22(2);
      break;
    case 3:
      onClickMovement_slide21(2);
      break;
  }
}

